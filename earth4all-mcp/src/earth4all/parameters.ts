export function getDefaultParameters(sector: SectorId): SectorParameters {
  return { ...DEFAULT_PARAMETERS[sector] };
}

export function getAllDefaultParameters(): Record<SectorId, SectorParameters> {
  const result: Record<string, SectorParameters> = {};
  for (const [sector, params] of Object.entries(DEFAULT_PARAMETERS)) {
    result[sector] = { ...params };
  }
  return result as Record<SectorId, SectorParameters>;
}
